package com.english.englishproject;

public class Users {
    private String email;
    private String fullname;
    private String mynumberphone;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getMynumberphone() {
        return mynumberphone;
    }

    public void setMynumberphone(String mynumberphone) {
        this.mynumberphone = mynumberphone;
    }
}
